ll CSOD(ll n){
    ll ret=0ull;
    for(ll i=2;i*i<=n;i++){
        ll j = n/i;
        ret+= j*(j+1)/2 - i*(i-1)/2;
        ret+= i*(j-i);
    }
    return ret;
}

